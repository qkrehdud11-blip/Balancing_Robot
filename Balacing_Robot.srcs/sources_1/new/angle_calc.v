`timescale 1ns / 1ps
//==============================================================================
// angle_calc — 상보 필터(Complementary Filter) 기울기 각도 추정 [초안]
//
// ▸ 출력 단위: 0.01° (centidegree)
//       1000 = +10.00°, -300 = -3.00°, 0 = 직립
//
// ▸ 상보 필터:
//       θ[n] = α × (θ[n-1] + Δθ_gyro) + (1-α) × θ_accel
//
// ▸ 각도 계산 방식 (소각도 근사):
//       θ_accel [0.01°] ≈ AX × 5730 / 16384 ≈ AX × 0.3498
//       Δθ_gyro [0.01°]  = GX [LSB] / 131 [LSB/(°/s)] × 0.01[s] × 100
//                        = GX / 131    (dt = 10ms 기준)
//
//       소각도 근사 오차: ±10° 이내 < 0.5%, ±20° 이내 < 2% → 실용적
//
// ▸ 내부 연산: Q8 고정소수점 (×256)
//       angle_q8 >> 8 = centidegree 단위 실제값
//
// ▸ 전제 조건:
//       data_valid 주기 = 10ms  (READ_PERIOD_TICKS=1000, tick=10us 기준)
//       입력은 bias 보정 완료 값 (top.v ax_corr, gx_corr 사용)
//
// ▸ 튜닝:
//       ALPHA_NUM 올릴수록 → 자이로 비중↑ (빠른 응답, 장기 드리프트 가능)
//       ALPHA_NUM 낮출수록 → 가속도 비중↑ (느린 응답, 진동 노이즈 취약)
//       권장 범위: 230(≈0.90) ~ 252(≈0.98)
//==============================================================================

module angle_calc
(
    input  wire               clk,
    input  wire               rst_n,

    input  wire               data_valid,    // mpu6050_ctrl에서 오는 10ms 주기 펄스

    input  wire signed [15:0] accel_x,       // bias 보정됨, 16384 = 1g
    input  wire signed [15:0] accel_z,       // 현재 미사용 (소각도 근사에서 AZ≈16384 가정)
                                             // 추후 정밀 arctan 구현 시 사용
    input  wire signed [15:0] gyro_x,        // bias 보정됨, 131 LSB/°/s

    output reg  signed [15:0] angle,         // 기울기 각도 [0.01°], 직립=0
    output reg                angle_valid    // 각도 갱신 완료 1클럭 펄스
);

    //--------------------------------------------------------------------------
    // 파라미터 (튜닝 가능)
    //--------------------------------------------------------------------------
    parameter signed [31:0] ALPHA_NUM  = 32'sd249;  // α = 249/256 ≈ 0.97
    parameter signed [31:0] ALPHA_COMP = 32'sd7;    // 1-α = 7/256 ≈ 0.03
    // ALPHA_NUM + ALPHA_COMP = 256 이 되어야 함

    //--------------------------------------------------------------------------
    // 내부 Q8 각도 레지스터
    // 실제값 [0.01°] = angle_q8 >>> 8
    //--------------------------------------------------------------------------
    reg signed [31:0] angle_q8;

    //--------------------------------------------------------------------------
    // 가속도계 기반 각도 (Q8 centidegree)
    //
    // 유도:
    //   소각도 가정 → sin(θ) ≈ θ_rad, cos(θ) ≈ 1
    //   AX = 16384 × sin(θ) ≈ 16384 × θ_rad
    //   θ_rad = AX / 16384
    //   θ [°]  = AX / 16384 × (180/π) = AX × 0.003497 × 180 = AX × 0.3497
    //   θ [0.01°] = AX × 34.97 ≈ AX × 35
    //
    //   Q8 변환: AX × 35 × 256 / 256 = AX × 35   ← 이미 Q8 스케일 필요
    //   → accel_q8 = AX × 35 × 256 / 256 = AX × 8960 / 256 = AX × 35
    //   (주의: Q8로 보관할 것이므로 실제는 AX × 35 × 256/256 = AX × 35)
    //
    //   Q8 단위로 표현하려면:
    //   θ_q8 = AX × 0.3497 × 256 = AX × 89.5 ≈ AX × 90
    //--------------------------------------------------------------------------
    wire signed [31:0] accel_q8 = $signed(accel_x) * 32'sd90;
    //   검증: AX=16384(1g, 90°) → 16384×90 = 1,474,560 → >>8 = 5760 centideg = 57.6°
    //   (소각도 근사 한계: 90° 실제값과 차이 발생, 30° 이내에서 오차 <2%)

    //--------------------------------------------------------------------------
    // 자이로 기반 각속도 × dt (Q8 centidegree)
    //
    // 유도:
    //   ω [°/s] = GX [LSB] / 131 [LSB/(°/s)]
    //   Δθ [°]  = ω × dt = GX / 131 × 0.01s
    //   Δθ [0.01°] = GX / 131 × 0.01 × 100 = GX / 131
    //
    //   Q8 변환: Δθ_q8 = GX × 256 / 131 ≈ GX × 1.954 ≈ GX × 2
    //
    //   (Vivado는 상수(131) 나눗셈을 곱셈+시프트로 최적화)
    //--------------------------------------------------------------------------
    wire signed [31:0] gyro_q8 = $signed(gyro_x) * 32'sd256 / 32'sd131;
    //   검증: GX=131(1°/s) → 131×256/131 = 256 → >>8 = 1 centideg/step
    //         10ms 후 → 1 centideg = 0.01°, 즉 0.01° × 100Hz = 1°/s ✓

    //--------------------------------------------------------------------------
    // 상보 필터 FSM
    //--------------------------------------------------------------------------
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            angle_q8    <= 32'sd0;
            angle       <= 16'sd0;
            angle_valid <= 1'b0;
        end
        else begin
            angle_valid <= 1'b0;

            if (data_valid) begin
                //--------------------------------------------------------------
                // 상보 필터 연산 (Q8)
                //   α × (prev_angle + gyro_step) + (1-α) × accel_angle
                //   >> 8: Q8 정규화
                //--------------------------------------------------------------
                angle_q8 <= (ALPHA_NUM  * (angle_q8 + gyro_q8)
                           + ALPHA_COMP * accel_q8) >>> 8;

                //--------------------------------------------------------------
                // 출력: Q8 → centidegree
                // angle_q8[23:8]: 비트 23~8 추출 (>>8과 동일, 부호 유지)
                // ※ 1클럭 지연 (현재 angle_q8는 이전 사이클 값)
                //--------------------------------------------------------------
                angle       <= angle_q8[23:8];
                angle_valid <= 1'b1;
            end
        end
    end

endmodule

